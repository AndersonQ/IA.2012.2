package ia;

public class Station 
{
	/**
	 * Class to represent stations
	 * to go from UFABC to USP
	 */
	protected String name;
	protected Colour c;

	public String getName() 
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public Colour getC()
	{
		return c;
	}

	public void setC(Colour c)
	{
		this.c = c;
	}
}
